	CD3DMesh						*m_pMesh;	// 모델

	D3DXMATRIX						m_mWorld;	// 월드 행렬
	D3DXMATRIX						m_mView;	// 뷰 행렬
	D3DXMATRIX						m_mProj;	// 투영 행렬

    FLOAT						m_fWorldRotX;	// X축회전
    FLOAT						m_fWorldRotY;	// Y축회전

	BOOL						m_bLoadingApp;	// 로드중?
